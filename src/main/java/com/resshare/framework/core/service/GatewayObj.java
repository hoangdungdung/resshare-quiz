package com.resshare.framework.core.service;

public class GatewayObj {
	
 
		 private String ip;
		 private String module;
		 private String path;
		 private int port;


		 // Getter Methods 

		 public String getIp() {
		  return ip;
		 }

		 public String getModule() {
		  return module;
		 }

		 public String getPath() {
		  return path;
		 }

		 public int getPort() {
		  return port;
		 }

		 // Setter Methods 

		 public void setIp(String ip) {
		  this.ip = ip;
		 }

		 public void setModule(String module) {
		  this.module = module;
		 }

		 public void setPath(String path) {
		  this.path = path;
		 }

		 public void setPort(int port) {
		  this.port = port;
		 }
		}


